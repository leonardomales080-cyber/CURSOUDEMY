//var, variables globales
var saludo = 'Hola Leonardo'
if(saludo){
    var saludo = 'HOLA'
    console.log(saludo)
}

console.log(saludo)
//let, variables limitadas al alcance del bloque donde las definimos
let nombre = 'Leonardo'
console.log(nombre)
if(nombre){
    let nombre = 'Leonardo Males'
    console.log(nombre)
}

//const, variables inmutables
const PI = 3.14
console.log(PI)